const fs = require('fs');
const path = require('path');
const { app } = require('electron');

let langDir = null;

function getLangDir() {
  if (!langDir) {
    langDir = path.join(app.getPath('userData'), 'lang');
    if (!fs.existsSync(langDir)) {
      fs.mkdirSync(langDir, { recursive: true });
    }
  }
  return langDir;
}

